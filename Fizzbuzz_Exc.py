    if fizzbuzz % 3 == 0  and fizzbuzz % 5 == 0:
        print("boringbuzz")
        continue
    elif fizzbuzz % 3 == 0:
        print("boring")
        continue
    elif fizzbuzz % 5 == 0:
        print("buzz")
        continue
        print(fizzbuzz)
